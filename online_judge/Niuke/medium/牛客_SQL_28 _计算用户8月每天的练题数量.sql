1.统计2021年8月每一天的练题数量
2.按日期顺序输出结果
3.只统计2021年8月的数据

特殊要求：无

解题思路：

1.时间筛选：只取2021年8月的数据
2.日期提取：从完整日期中提取"日"（day部分）
3.分组统计：按天分组，统计每天的题目数量
4.排序输出：按日期顺序输出

*/

-- ========== 我的解答 ==========
select 
  day(date) as day, 
  count(question_id) question_cnt 
from 
  question_practice_detail 
where 
  year(date)=2021 and month(date)=8 
group by 
  day(date);
